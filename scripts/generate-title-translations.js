#!/usr/bin/env node
/**
 * generate-title-translations.js
 *
 * Translates all story titles into Gujarati + Transliteration.
 * Batches 20 titles per Gemini call for efficiency.
 * Outputs: ../title-translations.js
 *
 * Usage:
 *   node scripts/generate-title-translations.js
 */

'use strict';

const fs   = require('fs');
const path = require('path');

const ROOT = path.join(__dirname, '..');

// ── Read Gemini API key from config.js ──────────────────────────────────────
let GEMINI_KEY = '';
try {
  const cfgSrc = fs.readFileSync(path.join(ROOT, 'config.js'), 'utf8');
  const m = cfgSrc.match(/geminiKey\s*:\s*['"]([^'"]+)['"]/);
  if (m) GEMINI_KEY = m[1];
} catch (e) { console.error('Could not read config.js:', e.message); }
if (process.env.GEMINI_KEY) GEMINI_KEY = process.env.GEMINI_KEY;
if (!GEMINI_KEY) { console.error('No Gemini API key found.'); process.exit(1); }

// ── Load stories ────────────────────────────────────────────────────────────
let STORIES_DATA;
{
  const src = fs.readFileSync(path.join(ROOT, 'stories-data.js'), 'utf8');
  eval(src.replace(/\bwindow\b/g, 'global'));
  STORIES_DATA = global.STORIES_DATA;
}

const allStories = [];
for (const stories of Object.values(STORIES_DATA.stories)) {
  for (const s of stories) {
    allStories.push({ id: s.id, title: s.title });
  }
}
console.log(`Found ${allStories.length} stories to translate titles for.\n`);

// ── Progress file ────────────────────────────────────────────────────────────
const PROGRESS_FILE = path.join(ROOT, '.title-trans-progress.json');
let progress = {};
try {
  progress = JSON.parse(fs.readFileSync(PROGRESS_FILE, 'utf8'));
  const done = Object.keys(progress).length;
  console.log(`Resuming — ${done} done, ${allStories.length - done} remaining.\n`);
} catch {}

// ── Translate a batch of titles ──────────────────────────────────────────────
const BATCH_SIZE = 20;

async function translateBatch(batch) {
  const numbered = batch.map((s, i) => `${i + 1}. "${s.title}"`).join('\n');
  const prompt = `Translate these ${batch.length} story titles into Gujarati script and Roman transliteration.

Return ONLY valid JSON — no markdown, no extra text:
[
  {"gujarati": "Gujarati script title", "transliteration": "Phonetic English"},
  ...
]

English titles:
${numbered}`;

  const url = `https://generativelanguage.googleapis.com/v1beta/models/gemini-2.5-flash:generateContent?key=${GEMINI_KEY}`;
  const body = JSON.stringify({
    contents: [{ role: 'user', parts: [{ text: prompt }] }],
    generationConfig: { responseMimeType: 'application/json', temperature: 0.2, maxOutputTokens: 8192 },
  });

  const MAX_ATTEMPTS = 3;
  for (let attempt = 0; attempt < MAX_ATTEMPTS; attempt++) {
    if (attempt > 0) await sleep(attempt * 4000);
    try {
      const { default: fetch } = await import('node-fetch').catch(() => ({ default: globalThis.fetch }));
      const res = await fetch(url, { method: 'POST', headers: { 'Content-Type': 'application/json' }, body });
      const data = await res.json();
      if (!res.ok) throw new Error(data?.error?.message || `HTTP ${res.status}`);
      const raw = data.candidates?.[0]?.content?.parts?.[0]?.text;
      if (!raw) throw new Error('Empty response');
      const cleaned = raw.replace(/^```json\s*/i, '').replace(/```\s*$/, '').trim();
      const result = JSON.parse(cleaned);
      if (!Array.isArray(result) || result.length !== batch.length) throw new Error('Wrong array length');
      return result;
    } catch (e) {
      if (attempt < MAX_ATTEMPTS - 1) { console.error(`  batch error: ${e.message} — retrying`); continue; }
      throw e;
    }
  }
}

function sleep(ms) { return new Promise(r => setTimeout(r, ms)); }

async function run() {
  const remaining = allStories.filter(s => !progress[s.id]);
  let processed = 0, errors = 0;

  for (let i = 0; i < remaining.length; i += BATCH_SIZE) {
    const batch = remaining.slice(i, i + BATCH_SIZE);
    process.stdout.write(`[${Object.keys(progress).length + 1}–${Object.keys(progress).length + batch.length}/${allStories.length}] Translating ${batch.length} titles… `);
    try {
      const results = await translateBatch(batch);
      batch.forEach((s, idx) => { progress[s.id] = results[idx]; });
      processed += batch.length;
      console.log('✓');
      fs.writeFileSync(PROGRESS_FILE, JSON.stringify(progress, null, 2));
    } catch (e) {
      errors += batch.length;
      console.error(`✗ ${e.message}`);
    }
    if (i + BATCH_SIZE < remaining.length) await sleep(800);
  }

  console.log(`\n✓ Translated ${processed} titles. ${errors} errors.\n`);

  // Write output
  const outPath = path.join(ROOT, 'title-translations.js');
  const js = `/* ============================================================
   mySanskar — Pre-generated story title translations
   Generated by: node scripts/generate-title-translations.js
   Total: ${Object.keys(progress).length} titles
   ============================================================ */

window.STORY_TITLE_TRANSLATIONS = ${JSON.stringify(progress, null, 2)};
`;
  fs.writeFileSync(outPath, js, 'utf8');
  console.log(`Wrote ${outPath} (${(fs.statSync(outPath).size / 1024).toFixed(1)} KB)`);
  console.log(`${Object.keys(progress).length}/${allStories.length} titles covered.\n`);

  if (Object.keys(progress).length === allStories.length) {
    try { fs.unlinkSync(PROGRESS_FILE); } catch {}
    console.log('All done! Commit title-translations.js and deploy.');
  }
}

run().catch(e => { console.error('Fatal:', e); process.exit(1); });
